 */
class SecurityReasonSearch extends SecurityReason
{
    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [
            [['reason_type'], 'integer'],
            [['description', 'created_at', 'updated_at'], 'safe'],
        ];
    }

